function getCharacterResponse(charId, userMessage, conversationCount) {
  const char = CHARACTER_RESPONSES[charId];
  if (!char) return '……';

  const msg = userMessage.toLowerCase();
  let topicPool = [];

  if (msg.includes('樱花') || msg.includes('sakura') || msg.includes('花')) {
    if (char.sakura) topicPool.push(...char.sakura);
  }
  if (msg.includes('吃') || msg.includes('饭') || msg.includes('饿') || msg.includes('美食') || msg.includes('餐厅') || msg.includes('好吃') || msg.includes('请客') || msg.includes('烤肉') || msg.includes('火锅')) {
    if (char.food) topicPool.push(...char.food);
  }
  if (msg.includes('剑') || msg.includes('刀') || msg.includes('训练') || msg.includes('练习') || msg.includes('武术') || msg.includes('打架')) {
    if (char.sword) topicPool.push(...char.sword);
  }
  if (msg.includes('保护') || msg.includes('危险') || msg.includes('帮忙') || msg.includes('救') || msg.includes('安全') || msg.includes('敌人')) {
    if (char.protect) topicPool.push(...char.protect);
  }
  if (msg.includes('路明非') || msg.includes('师兄') || msg.includes('明非') || msg.includes('小路') || msg.includes('衰仔')) {
    if (char.lmf) topicPool.push(...char.lmf);
  }
  if (msg.includes('小魔鬼') || msg.includes('路鸣泽')) {
    if (char.little_devil) topicPool.push(...char.little_devil);
  }
  if (msg.includes('游戏') || msg.includes('打游戏') || msg.includes('玩') || msg.includes('电竞') || msg.includes('steam') || msg.includes('boss')) {
    if (char.game) topicPool.push(...char.game);
  }
  if (msg.includes('写作') || msg.includes('写书') || msg.includes('小说') || msg.includes('创作') || msg.includes('灵感') || msg.includes('码字') || msg.includes('稿子') || msg.includes('章节') || msg.includes('更新')) {
    if (char.writing || char.character) {
      if (char.writing) topicPool.push(...char.writing);
      if (char.character) topicPool.push(...char.character);
    }
  }
  if (msg.includes('科学') || msg.includes('物理') || msg.includes('量子') || msg.includes('相对论') || msg.includes('熵') || msg.includes('宇宙')) {
    if (char.science) topicPool.push(...char.science);
  }
  if (msg.includes('比喻') || msg.includes('人生') || msg.includes('青春') || msg.includes('时间') || msg.includes('回忆') || msg.includes('记忆')) {
    if (char.metaphor) topicPool.push(...char.metaphor);
  }
  if (msg.includes('八卦') || msg.includes('听说') || msg.includes('秘密') || msg.includes('内幕') || msg.includes('传言')) {
    if (char.gossip) topicPool.push(...char.gossip);
  }
  if (msg.includes('废柴') || msg.includes('没用') || msg.includes('失败') || msg.includes('废物') || msg.includes('垃圾')) {
    if (char.selfdep) topicPool.push(...char.selfdep);
  }
  if (msg.includes('建议') || msg.includes('怎么办') || msg.includes('怎么') || msg.includes('如何') || msg.includes('帮')) {
    if (char.advice) topicPool.push(...char.advice);
  }
  if (msg.includes('你好') || msg.includes('嗨') || msg.includes('hello') || msg.includes('hi') || msg.includes('在吗') || msg.includes('hello') || msg.includes('你好啊')) {
    if (char.greeting) topicPool.push(...char.greeting);
  }
  if (msg.includes('怎么样') || msg.includes('好不好') || msg.includes('是吗') || msg.includes('对吗') || msg.includes('是的') || msg.includes('是的吗') || msg.endsWith('吗') || msg.endsWith('么')) {
    if (char.question) topicPool.push(...char.question);
  }
  if (msg.includes('感觉') || msg.includes('心情') || msg.includes('难过') || msg.includes('开心') || msg.includes('寂寞') || msg.includes('害怕') || msg.includes('伤心') || msg.includes('孤单')) {
    if (char.feeling) topicPool.push(...char.feeling);
  }

  if (topicPool.length === 0) {
    if (char.casual) topicPool.push(...char.casual);
  }

  if (topicPool.length === 0) {
    if (conversationCount < 3) {
      if (char.intimate_low) topicPool.push(...char.intimate_low);
    } else {
      if (char.intimate_high) topicPool.push(...char.intimate_high);
    }
  }

  if (conversationCount >= 5 && char.intimate_high) {
    const intimateChance = Math.min(0.4, 0.1 + conversationCount * 0.03);
    if (Math.random() < intimateChance) {
      topicPool.push(...char.intimate_high);
    }
  }

  if (topicPool.length === 0) {
    return '……';
  }

  return topicPool[Math.floor(Math.random() * topicPool.length)];
}
